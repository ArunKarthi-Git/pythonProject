if __name__=="__main__":
    a=int(input("Enter the no."))
    if a==1:
        print(a,"ONE")
    elif a==2:
        print(a,"TWO")
    elif a==3:
        print(a,"Three")
    elif a==4:
        print(a,"Four")
    elif a==5:
        print(a,"Five")
    else:
        print(a,"Not single digit no.")